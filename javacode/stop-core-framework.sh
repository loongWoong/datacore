#!/bin/bash

# ==========================================
# 数据中台核心框架服务停止脚本 (Linux)
# ==========================================

echo "=========================================="
echo "数据中台核心框架服务停止脚本 (Linux)"
echo "=========================================="

# 查找并终止核心框架服务进程
echo "正在查找核心框架服务进程..."

# 终止监控服务
echo "正在停止监控服务..."
pkill -f "Monitoring Service"

# 终止网关服务
echo "正在停止网关服务..."
pkill -f "Gateway Service"

# 终止审计服务
echo "正在停止审计服务..."
pkill -f "Audit Service"

# 终止认证服务
echo "正在停止认证服务..."
pkill -f "Auth Service"

# 终止配置中心服务
echo "正在停止配置中心服务..."
pkill -f "Config Service"

# 终止服务注册与发现服务
echo "正在停止服务注册与发现服务..."
pkill -f "Discovery Service"

echo "=========================================="
echo "核心框架服务已停止"
echo "=========================================="